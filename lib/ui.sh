    desc=$1
    cmd=$2

    echo -ne "${CYAN}‚ě§ $desc...${RESET} "

    bash -c "$cmd" >> $LOG_FILE 2>&1 &
    spinner
    wait $!

    if [ $? -eq 0 ]; then
        echo -e "${GREEN}‚úĒ${RESET}"
    else
        echo -e "${RED}‚úĖ FAILED${RESET}"
        echo "Check log: $LOG_FILE"
        exit 1
    fi
}
