spinner() {
    local pid=$!
    local spin='-\|/'
    local i=0
    while kill -0 $pid 2>/dev/null; do
        i=$(( (i+1) %4 ))
        printf "\r${CYAN}[%c]${RESET} Installing..." "${spin:$i:1}"
        sleep .1
    done
    printf "\r"
}

