spinner() {
    local pid=$!
    local spin='‚†č‚†ô‚†ł‚†ī‚†¶‚†á'
    local i=0
    while kill -0 $pid 2>/dev/null; do
        printf "\r${CYAN}%s${RESET} " "${spin:$i:1}"
        i=$(( (i+1) %6 ))
        sleep 0.08
    done
    printf "\r"
}

